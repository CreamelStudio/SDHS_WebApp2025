  function showScreen(target){
    [screenList, screenQuiz, screenResult].forEach(s => s.classList.remove("active"));
    target.classList.add("active");
  }
  
  function calcScores(){
    const scores = {};
    currentTest.questions.forEach((q, qi)=>{
      const pi = picked[qi];
      if (pi == null) return;
      const sc = q.answers[pi].score;
      for (const k in sc){
        scores[k] = (scores[k] || 0) + sc[k];
      }
    });
    return scores;
  }
  
  function topResult(scores){
    let bestKey = null;
    let bestVal = -Infinity;
    for (const k in scores){
      if (scores[k] > bestVal){
        bestVal = scores[k];
        bestKey = k;
      }
    }
    return bestKey;
  }
  
  
