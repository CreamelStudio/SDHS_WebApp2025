  function startTest(id){
    currentTest = TESTS.find(t=>t.id===id);
    currentQIndex = 0;
    picked = new Array(currentTest.questions.length).fill(null);
  
    quizTitle.textContent = currentTest.title;
    quizDesc.textContent = currentTest.desc;
  
    showScreen(screenQuiz);
    renderQuestion();
  }
  
  function renderQuestion(){
    const qs = currentTest.questions;
    const q = qs[currentQIndex];
  
    qNum.textContent = `Q${currentQIndex+1}`;
    qText.textContent = q.q;
  
    // progress
    const percent = (currentQIndex / qs.length) * 100;
    progressFill.style.width = percent + "%";
    progressText.textContent = `${currentQIndex+1} / ${qs.length}`;
  
    // answers
    answerCon.innerHTML = "";
    q.answers.forEach((a, idx)=>{
      const btn = document.createElement("button");
      btn.className = "answer-btn";
      btn.textContent = a.text;
  
      if (picked[currentQIndex] === idx) btn.classList.add("selected");
  
      btn.addEventListener("click", ()=>{
        picked[currentQIndex] = idx;
        renderQuestion();
        nextBtn.disabled = false;
      });
  
      answerCon.appendChild(btn);
    });
  
    prevBtn.disabled = (currentQIndex === 0);
    nextBtn.textContent = (currentQIndex === qs.length-1) ? "ê²°ê³¼ë³´ê¸°" : "ë‹¤ìŒ";
    nextBtn.disabled = (picked[currentQIndex] == null);
  }
  
  prevBtn.addEventListener("click", ()=>{
    if (currentQIndex > 0){
      currentQIndex--;
      renderQuestion();
    }
  });
  
  nextBtn.addEventListener("click", ()=>{
    const qs = currentTest.questions;
    if (currentQIndex < qs.length-1){
      currentQIndex++;
      renderQuestion();
    } else {
      finishTest();
    }
  });
  
  backBtn.addEventListener("click", ()=>{
    showScreen(screenList);
  });
  
  
