  function finishTest(){
    const scores = calcScores();
    const key = topResult(scores);
    const r = currentTest.results[key];
  
    progressFill.style.width = "100%";
    progressText.textContent = `${currentTest.questions.length} / ${currentTest.questions.length}`;
  
    resultIcon.textContent = r.icon;
    resultTitle.textContent = r.title;
    resultSummary.textContent = r.summary;
  
    resultTags.innerHTML = r.tags.map(t=>`<div class="result-tag">${t}</div>`).join("");
    resultDetail.textContent = r.detail.trim();
  
    showScreen(screenResult);
  }
  
